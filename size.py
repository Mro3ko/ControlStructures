size=input("Enter size symbol: ")
if size=="S":
    print("S: Small size")
elif size=="M":
    print("M: Medium size")
elif size=="L":
    print("L: Large size")
elif size=="XL":
    print("XL: Extra large size")
elif size=="XXL":
    print("XXL: Extra extra large size")
else:
    print(f"{size} symbol is incorrect")